		KDE Mover-Sizer
	</h1>
	<h2>
		<small><small>Move and resize Windows windows just like Linux Windows!</small></small>
	</h2>

	<h3>
		<small><small>An essential add-on for Windows XP, 2K, Windows Server, Windows 7 and Windows 8/8.1.<br />
		Yeah, okay, <em>and Vista!</em></small></small>
	</h3>

	Here is a page about my KDE Mover-Sizer. Okay, it's not entirely mine, not even mostly, but I've added enough code to call it mine, and it's too damn essential to not have a page of its own somewhere, so here it is..<br />
	<br />

	<h3>What does it do?</h3>

	KDE Mover-Sizer is a background application that emulates the behaviour of KDE, which is a rather good Linux desktop environment. Actually, Gnome and other Linux window managers also do it these days, but that wouldn't make for such a funky name. Essentially, you hold down the Alt key, and Left-click to move a window, Right-click to resize it; and <em>from anywhere inside the window.</em> That's it. And once you use it for a few minutes, you'll wonder how you ever lived without it.<br />
	<br />

	The utility itself is coded with AutoHotKey, originally a fork of AutoIt, and highly useful in its own right. The original script (which I snaffled from the AutoHotKey forum, the best of many similar scripts) did all the above, but was missing something essential, that is; window snapping. So I added that, and gave them it back. <br />
	<br />

	The window snapping is important for at least two reasons; 1) it enables you to place a window, as if by magic, <em>exactly</em> at the edge of your desktop. If, like me, you like to keep your main document windows in the centre of your screen, and leave lots of folder windows open up and down the sides of your desktop (I have a widescreen monitor now, which makes this even more effective) then you will find it invaluable. And 2) it enables you to resize the window <em>from the edge of your screen.</em> This is easier to do than to explain, though I'm going to attempt that anyway, with a couple of how-to style tips..<br />
	<div id="section-Tricks" class="space-tiny"></div>

	<h3>Cool Tricks..</h3>

	I've gotten into a couple of habits thanks to the KDE Mover-Sizer. The first is a quick one-two action where I first Alt-Left-click a window and throw it roughly into place (off the edge of the screen), and then do a single Alt-Right-click to snap it back into perfect view. I've already had a week-off with all the time this good habit has saved me.<br />
	<br />

	The new version can also snap directly to the edge during regular Alt-Left-Click moving - so long as you are within the snap distance, it will lock against the edge of your desktop.<br />
	<br />

	The second habit of one of resizing windows <em>from the edge.</em> First, I get them there, as in tip 1, then I grab a corner (anywhere in the quadrant is fine) with an Alt-Right-click, and drag-resize them while the two opposite edges (one of which is bang up against the edge of the desktop) stay put. This isn't so much a time saver, as a sanity saver. I'm very particular about the amount of white space that shows in folder windows, and it they don't look right, I couldn't leave them open. Leaving them open is what saves time.<br />
	<br />

	Another thing I've started doing, is sliding windows up and down the edge of my desktop by Alt-Left-click+drag (at the very edge of the desktop). The snap keeps them from moving left and right - it's like they are on rails! Very handy. And remember, it works on windows behind windows, too, and without bringing them to the front. <br />
	<br />
	<h3>Try it!</h3>
	You need to play with it to fully appreciate the beauty of KDE Mover-Sizer. Fortunately, I have a precompiled Windows executable which you can simply download and run. There's no installer, and it doesn't use the registry. The only settings are in a regular plain text ini file, which lives right next to KDE Mover-Sizer.exe, so it's completely portable. When running, a cute tray icon enables you to exit, if required (unlikely!), as well as a few other options. Drop a shortcut into your startup folder, and then you're cooking with KDE!<br />
	<br />
	<h3>Then forget about it..</h3>
	KDE Mover-Sizer uses very little resources to work its magic, and works on all desktop windows, even windows that aren't at the front, even those daft fullscreen installer windows. It will even sneak inside a Sandboxie and work there. It also enables you to resize windows that don't normally allow resizing, which can be a real bonus with certain dumb dialogs <small>(cough *FirefoxSearchPlugin* cough)</small>. In no time, it feels perfectly natural, and you're throwing windows around with a whole new level of precision and abandon!<br />
	<br />

	Like I say, I "couldn't live without it", and I'm fairly certain that if you use it for a wee while, you will feel the same way. At any rate, it's free; so why not try it and see..<br />


