        <h2><img src="images/pawred.gif" width="16" height="16" alt="Red paw print" /> What is Dog Rescue?</h2>
        <p>Below is a simplified example of the rescue process. <br /> <span style="font-size:11px">(Each shelter/ rescue group has different procedures and each state has different regulations.)</span></p>
        <p> 1) Dog enters pound.</p>
        <p>2) Pound sets date for dog to be put to sleep (it could be as quick as 1 week for unchipped dogs in NSW).</p>
        <p>3) During the week the dog can be reclaimed by the owner.</p>
        <p>4) If the dog is not reclaimed after 1 week it is either; </p>
          <blockquote>
            <p>a) given extra time for someone to adopt or reclaim it 
              (if resources are available),</p>
            <p>b) transferred to a rescue group, or</p>
            <p>c) put to sleep.</p>
          </blockquote>
        <p> <img src="images/dog-running-with-ball.jpg" alt="dog running with ball" style="border-radius: 10px; float:right; margin-bottom:10px; margin-left:10px;" class="mobilehide"/> If given to a rescue group the dog is put into foster care. It is <a href="desex.php">desexed</a>, vaccinated, given health care and potential new families can view the dog. If they like the dog, they adopt the dog.</p>
        <p>Dog foster carers are people who rescue dogs from being put to sleep in animal shelters. They temporarily provide food, care, and shelter for the dog in their own home until a permanent home can be found.<br />
    <br />Dogs have a limited amount of time before a pound or shelter will have to put them to sleep.</p>
        <p>Being a foster care provider takes a considerable amount of time, dedication, and genuine caring. It's a big commitment and it's not a job for everyone. Yet the fulfillment and sense of purpose you receive in knowing that you helped one more dog find its way into a safe, happy home is most beneficial 
each time you successfully place a dog.</p>
        <p>Australia has one of the highest rates of dog ownership in 
            the world and dogs play an important part in family's and 
            the community. There are a small number of people who (for 
            any number reasons) let their dogs end up in animal shelters. 
            These dogs do not deserve to be put to sleep, most of them 
            are perfectly healthy and <a href="about-dog-fostering.php">foster carers</a> give them a second 
        chance at living.</p>
