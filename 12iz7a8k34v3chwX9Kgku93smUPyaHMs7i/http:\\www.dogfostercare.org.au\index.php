      <h1>Dog Foster Care links to dog rescue groups who require volunteer foster carers.</h1>
        <p>The number of dogs that can enter foster care with a rescue group is limited by the number of volunteer homes available. </p>
        <p>If you're thinking about dog fostering, please take the time to research and then contact a group near you: <a href="dog-rescue-groups-act.php">ACT</a>, <a href="dog-rescue-groups-northern-territory.php">NT</a>, <a href="dog-rescue-groups-nsw.php">NSW</a>, <a href="dog-rescue-groups-queensland.php">QLD</a>, <a href="dog-rescue-groups-south-australia.php">SA</a>, <a href="dog-rescue-groups-tasmania.php">TAS</a>, <a href="dog-rescue-groups-victoria.php">VIC</a>, or <a href="dog-rescue-groups-western-australia.php">WA</a>.</p>
