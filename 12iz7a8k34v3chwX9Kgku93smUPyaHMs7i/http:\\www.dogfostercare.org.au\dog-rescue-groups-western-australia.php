        <h1>Western Australian Dog Rescue Links</h1>
        <strong><img src="images/icon-tick.png" alt="Tick" /> Last updated 20-07-2014</strong>
        <p>Each group has different resources and procedures, please 
        contact them individually for more info.</p>
          <p style="font-size:12px">*<em>Listed in alphabetical order, <strong>location preference in bold</strong></em>* </p>
          <em><p style="font-size:12px">Notice: The Labor Party have launched a website, <a href="http://www.alp.org.au/cosmeticstesting" target="_blank">www.alp.org.au/cosmeticstesting</a>, where you can make a submission or get further information on a phase out of the importation, manufacture, sale and advertising of cosmetics or <strong>cosmetic ingredients tested on animals</strong>.</p></em>
      <p><a href="http://www.australianfostercarersnetwork.org/" target="_blank">Australian Foster Carers Network</a> (AFCN)<br />
        National animal rescue group and rescue support network. Carers always needed for dogs  in all locations in Australia, <strong>all states</strong>.<br />
      <span class="smaller">Rescuing since:  2013</span>
      </p>
          <p><a href="http://narga.org.au/" target="_blank">NARGA</a> (National Animal Rescue Groups of Australia)<br />
          We are looking for foster carers nationwide to join our foster database. We currently work with over 50 rescue groups <strong>throughout Australia</strong> and are committed to helping our rescue groups find loving foster homes. Fostering is both a heart-warming and rewarding experience so please join us in helping to provide an important stepping stone to find pet a forever home.<br />
      <span class="smaller">Rescuing since:  2012</span></p>
          <p><a href="http://www.rspcawa.asn.au/get-involved/fostering.html" target="_blank">RSPCA 
            Western Australia</a><br />
        Looking for volunteers in <strong>Western Australia</strong>.</p>
          <p><a href="http://www.safe.asn.au" target="_blank">Saving Animals 
            From Euthanasia Inc (SAFE)</a><br />
            Zero Euthanasia for Rehomable Pets and quality of life for all companion animals...<br />
            SAFE was founded in the Pilbara in Western Australia by Sue Hedley in February 2003. Since then SAFE has developed branches and networks across the state – and intend to extend SAFE's services wherever there is need. Looking for foster carers <strong>throughout WA</strong>.<br />
          <span class="smaller">Rescuing since:  2003</span></p>
          <p><a href="http://www.sharpeirescue.org/" target="_blank">Shar Pei Rescue</a><br />
          Breed specialist rescue focusing on Shar Pei and <strong>operating nationally</strong>.  Winner of the 2011 MDBA Master Rescuer Award.<br />
          <span class="smaller">Rescuing since: 2009 </span>          </p>
          <p><a href="http://www.southwestanimalrescue.com" target="_blank">South 
            West Animal Rescue</a><br />
            Based <strong>in Bunbury</strong>, we work on a foster care basis and aim to 
            rescue, rehabilitate and rehome unwanted domestic animals. 
            We also promote the benefits of sterilising animals, and choosing 
            the correct dog breed choice for your family.<br />
            <span class="smaller">Rescuing since:  2006</span></p>
          <p><a href="https://www.facebook.com/staffyandbullybreedrescue" target="_blank">Staffy and Bully Breed Rescue Group</a><span style="font-size:12px"> &lt; Facebook link</span><br />
          We one of Western Australia's largest no kill rescue groups, which helps to rehome abandoned, displaced and surrendered bull breeds <strong>throughout the state</strong>. We do this by caring for and rehabilitating, staffy and bully type puppies and dogs in a home environment, until a loving new family comes along who wishes to adopt. We also aim to educate the public on responsible ownership of all dogs and the benefits of adopting, over purchasing a dog through a pet shop or back yard breeder. We are a non funded, not for profit registered charity. <br />
            <span class="smaller">Rescuing since:  2008</span></p>
