<h2><span class="mw-headline" id="Episode_Outlines">Episode Outlines</span></h2>
<h3><span class="mw-headline" id="Episode_One">Episode One</span></h3>
<p>Professor is in ENIS and fighting Noland in a modified grunt mech. The two are pretty evenly matched, until the Professor uses ENIS' special move, but Noland gets the upper hand and sends him plummeting to the ground below. As he falls, the professor shouts some bad-ass line about how 'He'll finish off the Lions, even after he has died.' 
</p><p>Ohta is mining. Suddenly, a huge quake hits that causes a cave-in. He finds himself trapped by the cave-in and heads through the mines and finds that the quake was caused by the crash-landed ENIS. The professor, who thought he was going to die alone, begins laughing even as he bleeds, because here's someone who he can make into a pilot. He shoves Ohta into the machine and forces the piloting control into his hand, his final act and a way of saying "FUCK YOU" to the Lions in passing. The Professor tells Ohta there's going to be a Lion Empire invasion, and sees him off before passing away.
</p><p>As Ohta and ENIS head for their home city, and find it under attack by one of the Lion Empire's racial fleets. He fights them off in the city for a bit before almost losing to the Super leading the invasion, but manages to escape. He picks up a garbled message from the Evaikari government, and decides to escort evacuating ships. Thus Ohta leaves the planet, while a massive Lion Empire fleet orbits and Noland realizes that ENIS survived and thinks the Professor survived too.
</p>
<h3><span class="mw-headline" id="Episode_Two">Episode Two</span></h3>
<p>Ohta fights off an enemy attack on the evacuating fleet as it rushes towards the edge of the system and the hyperspace routes towards FAP. The Super from the first episode returns and Ohta, still not familiar with ENIS, is almost killed, but is saved by Daisuke Ryokija in Wingschneider. White Knight picks up Ohta and he meets the scientist crew, who will eventually become Project Enis. They decide to help him escort the civilians to safety. Some discussion with the scientists reveals some of ENIS' potential to Ohta, but before he can learn more they reach the edge of the system and fight off an attack by the Grays. Ohta manages to finally get the upper hand on Im.C.M.L and defeats it, but just as White Knight is preparing to jump into hyperspace, it is knocked off its launch trajectory and sent rocketing off into Lion Empire territory.
</p>
