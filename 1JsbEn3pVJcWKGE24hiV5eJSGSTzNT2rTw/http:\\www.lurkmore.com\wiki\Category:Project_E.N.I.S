<p><i>Welcome to <b>Project E.N.I.S.</b>, a <a href="/wiki//m/" title="/m/">/m/-original mecha</a>.</i>
</p><p>Project E.N.I.S originated in the /m/echa section of <a href="/wiki/4chan" title="4chan" class="mw-redirect">4chan</a>. It all began as a project to create a new mecha by adding shapes &amp; components to a single triangle. After only a few parts were added, some <a href="/wiki/Anonymous" title="Anonymous">Anonymous</a> wrote the word "PENIS" on its chest.  But, instead of removing it, the /m/echanoids went along with it and worked the "P" into the design. This left the word "ENIS" on the left part of its chest, thus giving the unit its unique name. 
</p><p>This single thread sparked a wildfire of artistic and literary creations on /m/, with all kinds of /m/-original mecha being created and fleshed out to the great delight of our /m/etalheads.
</p>
